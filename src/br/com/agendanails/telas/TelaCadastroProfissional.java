    private void salvarProfissional() {
        try {
            String nome = txtNome.getText().trim();
            String especialidade = txtEspecialidade.getText().trim();
            String horario = txtHorario.getText().trim();
            
            if (nome.isEmpty() || especialidade.isEmpty() || horario.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Preencha todos os campos!");
                return;
            }
            
            Profissional profissional = new Profissional(nome, especialidade, horario);            
            ProfissionalDAO dao = new ProfissionalDAO();
            
            dao.inserir(profissional);
            
            javax.swing.JOptionPane.showMessageDialog(this, "Profissinal cadastrado com sucesso!");
            
            if (telaProfissionais != null) {
                telaProfissionais.atualizarTabela();
            }
            
            this.dispose();
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao salvar: " + e.getMessage());
        }
    }
        

